'''
Python’s print() function comes with a parameter called ‘end’. By default, the value of this parameter is ‘\n’, i.e. the new line character.

Expected Output :

Rahul
301, ransto hemlet street, 45841

'''

print('Rahul')
print('301, ransto' , end=" ")
print('hemlet street, 45841')

print('09','12','2016', sep='-')

# Due to end operator second and third statement printed on one line